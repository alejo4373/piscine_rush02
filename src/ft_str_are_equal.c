int	ft_str_are_equal(char *s1, char *s2)
{
	while (*s1 != '\0' && *s2 != '\0' && *s1 == *s2)
	{
		s1++;
		s2++;
	}
	if (!(*s1 - *s2))
	{
		return (1);
	}
	return (0);
}
